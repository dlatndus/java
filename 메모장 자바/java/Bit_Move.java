public class Bit_Move{
	public static void main(String args[]){
		System.out.println(6<<2); //6*2*2=24
		System.out.println(28>>2); //9 4
		System.out.println(-28>>2); // -5
		System.out.println(28>>>2);
		System.out.println(-28>>>2); //0000000000000000000000000010010 001111111111111111111111111011
		System.out.println(28/4);
		System.out.println(-28/4);
	}
}